            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><strong><u>1. Salutation 
</u></strong><div>The most common salutation in an informal letter is “Dear….” 
</div><div>Note that it is followed by a comma. 
</div><div>Dear Mimi, 
</div><div>Always try to use the nick name or the first name of the person you’re writing to, in the informal letters. 
</div><div>However, some go extremely informal and use “Hey!” or “Hi!” such type of use is not allowed in the examination. 
</div><div><strong><u>2. Body </u></strong>
</div><div>Here are a few things you should take note of: 
</div><div>1. Use informal language 
</div><div>The first paragraph generally expresses a greeting, followed by wishes of good health. Remember you are writing to someone you know very well, so try to be as friendly as possible but don’t become so much friendly and you start using the extreme use of colloquial words and abusing words. A good start: 
</div><div><strong>“How are you my dear sister?”</strong> 
</div><div>Try not to go overboard. Some people become bold and daring in letters and write things that they would otherwise not say to the person face to face. Obviously if you are writing to an adult that you respect, like your dad, try not to write things like: 
</div><div><strong>“What’s up dude!” or “What’s going down?”</strong></div><div><strong><br></strong></div><div><strong> 
</strong></div><div>Try to picture the person you are writing to standing in front of you. Imagine the things that you would say to him and write them down. This will help you not to go overboard. 
</div><div>Also avoid boring sentences like… 
</div><div>“I am writing this letter to….…” unless you are writing to a stranger. Even so, try to be as amiable as possible. Have a look at the following example: 
</div><div>“I have heard so much about you and would be head over heels with joy if you could agree to be my pen pal”…. 
</div><div>Try to be as conversational as possible. You are allowed to use colloquial language – i.e. language that is appropriate for speech but not really for writing, but not use it too much: 
</div><div>“My journey back here was fine, though it was quite a long one. I wanted to travel by CR bus but guess what; all the wretched buses were full! So I had no choice but to travel by a small Rosa bus. The journey took seven hours. By the time we reached, my legs were tried and my bottom was severely sore, ugh! Next time, I promise, I’m not gonna use one of ‘em tiny buses!” 
</div><div>2. Punctuation and spelling 
</div><div>However informal you get, you should not forget to pay attention to punctuation and spelling. I have come across a good number of letters that abound with spelling mistakes and awful punctuation. Such mistakes tend to distract the reader, so don’t neglect them. 
</div><div>If you use contractions, make sure that you put the apostrophe in the right places. For example: 
</div><div>1. Isn’t and not is’nt 
</div><div>2. Won’t and not wont 
</div><div>3. Mustn’t and not must’nt 
</div><div>The contraction (it’s) is especially one that you must watch out for. It is the short form of it is or it has. But if you want to use it to indicate possession, you should use its and not it’s. Check out this example: 
</div><div>“The dog lost its collar”. 
</div><div>Remember also to use capitals for the right things i.e. the names of people, places, holidays, etc should all start with a capital letter. 
</div><div>“Bottom line? Don’t throw away your grammar book!” 
</div><div><br><strong>3. <u>Use consistence voice and style</u> 
</strong></div><div>Use the active voice if you want your letter to sound more conversational and interesting. Avoid shifts in the voice. One common error is inconsistency in the tense. For example read the following sentence: 
</div><div>“I was going to town yesterday when a dog bite me and I ran all the way to the hospital.” 
</div><div>Here is a sentence with starts in the past tense and then right in the middle, the tense changes to present and then finally reverts to past. This is distracting! 
</div><div><br><strong>4</strong>.<strong> <u>Ask questions </u>
</strong></div><div>It is always a good idea to ask questions in the body of the letter that you would like the person to answer in their reply. Questions work as a good base on which to write a letter, and they give the recipient motivation to reply: 
</div><div>“How are those wonderful brothers of mine?” 
</div><div>You can also use tag questions in the letter like the one given below as they makes the letter much more outstanding. 
</div><div>“Did I tell you that I am librarian too, eh?” 
</div><div>“You also like such kind of bash, don’t you?” 
</div><div>
</div><div><strong>5. </strong><u><strong>Paragraphs</strong> 
</u></div><div>Paragraphing is one of the most important things required in the examination. You need to write single paragraph to explain one reason / factor. You should consume your about 100 words on each of the paragraph in examination so as there are 3 points given so the 3 paragraphs should include round about 300 words and there should be round about 15-20 words in the opening and closing part of the letter.
</div><div><br><strong>6. <u>Forbidden words in Informal writing: 
</u></strong></div><div>There are some words whom use is totally not allowed, so we must ignore them in this writing. Those words are all the formal words and the use of “which” and “that”. We can replace formal words by changing them into informal words or converting into simple English. Often the meaning of a sentence does not change when these words are removed: that and which.
</div><div><strong><br></strong></div></div>
        </div>
    </div>
