            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7">The Cambridge International AS and A Level Business syllabus enables learners to apply their business knowledge and understanding in order to analyze and present information, give reasoned explanations, and make judgments and recommendations.&nbsp;<div>The sole purpose of the notes is to ensure that students succeed. AS and A Level Business 9609 Revision Notes are concise and manageable for students preparing for their Cambridge Assessment International Education Examination.&nbsp;</div><div><br></div><div><strong>AS Business 9609 Cost and Finance Formula Sheet
</strong></div><div>
</div><div>FIXED COSTS =  costs which do not vary with output or sales.  If sales increase then fixed costs remain the same.  When there are zero sales, fixed costs still need to be paid.  Eg: Rent, cleaning materials, insurance.  Also called INDIRECT COSTS OR OVERHEAD COSTS.
</div><div><br>VARIABLE COSTS = costs that vary with output and sales.  Also called DIRECT COSTS.  They can be directly identified with a particular product
</div><div><br>TOTAL COSTS = FIXED COSTS + VARIABLE COSTS
</div><div><br>REVENUE OR SALES =  income earned from sales or services
</div><div><br>TOTAL (SALES) REVENUE = PRICE X QUANTITY SOLD
</div><div><br>BREAK-EVEN POINT = the level of sales where TOTAL COST = TOTAL REVENUE
</div><div><br>FINAL ACCOUNTS made up of 3 things – INCOME STATEMENT (TRADING ACCOUNT, PROFIT AND LOSS ACCOUNT) and STATEMENT OF FINANCIAL POSITION (BALANCE SHEET) AND CASH FLOW STATEMENT
</div><div><br>TRADING ACCOUNT = SHOWS HOW THE GROSS PROFIT OF A BUSINESS IS CALCULATED
</div><div><br>GROSS PROFIT = SALES REVENUE – COSTS OF GOODS SOLD
</div><div><br>COST OF GOODS SOLD = OPENING STOCK + PURCHASES – CLOSING STOCK
</div><div><br>PROFIT AND LOSS ACCOUNT SHOWS how THE NET PROFIT AND RETAINED PROFIT (OF A <br><br>LIMITED COMPANY) ARE CALCULATED
&nbsp;<span style="font-size: 1rem;">OPERATING (NET) PROFIT = GROSS PROFIT – OVERHEAD (FIXED COSTS) (+ non-trading income eg: rent,commission)&nbsp;</span></div><div><br>RETAINED EARNINGS ( PROFIT) = OPERATING (NET) PROFIT – TAX - DIVIDENDS
</div><div>STATEMENT IF FINANCIAL POSITION (BALANCE SHEET)  = SHOWS THE VALUE OF A BUSINESS AT A PARTICULAR TIME
</div><div><br>BALANCE SHEET GIVES A VALUE TO THE ASSETS AND LIABILITIES 
</div><div><br>ASSETS = ITEMS OWNED BY THE BUSINESS
</div><div><br>FIXED ASSETS = LAND, BUILDINGS, MOTOR VEHICLES
</div><div><br>CURRENT ASSETS =  CASH, STOCK, TRADE RECEIVABLES (DEBTORS (PEOPLE WHO OWE YOU MONEY))
</div><div><br>LIABILITIES  = ITEMS OWED BY THE BUSINESS
</div><div><br>LONG-TERM LIABILITIES = LOANS PAID BACK LONGER THAN 1 YEAR
</div><div><br>CURRENT LIABILITIES = PAID BACK WITHIN 1 YEAR EG: BANK OVERDRAFT, TRADE PAYABLES (CREDITORS (THE PEOPLE/SUPPLIERS YOU OWE MONEY TO))
</div><div>
</div><div><strong>BALANCE SHEET EQUATION:
</strong></div><div>TOTAL ASSETS – TOTAL LIABILITIES = OWNERS’ WEALTH (SHAREHOLDERS FUNDS)
</div><div><br>BALANCE SHEET TERMS EXPLAINED:
</div><div><br>WORKING CAPITAL  =  CURRENT ASSETS  - CURRENT LIABILITIES
</div><div><br>NET ASSETS = FIXED ASSETS + WORKING CAPITAL 
</div><div><br>SHAREHOLDERS FUNDS  = MONEY INVESTED INTO THE BUSINESS BY THE OWNERS (SHAREHOLDERS)
</div><div><br>Invested in two ways : 	1)	 Share capital = money put in business from new shares issued
</div><div>&nbsp; &nbsp;2)	Profit and Loss reserves=– retained profit from previous years
</div><div><br>CAPITAL EMPLOYED = SHAREHOLDERS FUNDS + LONG-TERM LIABILITIES
</div><div><br>WHAT DO ACCOUNTS TELL US?  
</div><div>HOW SUCCESSFUL  A COMPANY IS (profitability) AND PERFORMANCE OR FINANCIAL STRENGTH OF THE COMPANY.  WE USE SOME SIMPLE RATIOS TO FIND THIS OUT AND YOU MUST COMPARE THEM TO OTHER YEARS AND OTHER BUSINESSES. (on its own ratios mean very little)
</div><div><br></div><div><br></div><div>
</div><div><span style="font-size: 1rem;"></span>
