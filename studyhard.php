    <div class="container">
        <div class="media-container-row">
            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><div>First let me mention that this is a guide on how to prepare for exams and then appear in your examination. This is a general piece of advice on how to succeed, Insha’Allah (God-willing), and you must remember that for each subject there are specific guidelines and tricks which your teacher must tell you. But this guide is my attempt at writing a core piece of advice on how to ace exams from research and my experience with my own self. What you need to do months ahead of Exams: 
</div><div>Before you even start preparing for exams you must set a goal and set your attitude according. It’s about motivating yourself for the long run. For example, you want the scholarship for your dream university, or you want to make your parents proud or happy. Why you want to achieve good grades is an important question and important to every person in a different way. You need to think about it so you know what you are doing.
</div><div>And remember: "<strong>If you aim for the moon, you will land among the stars.
"</strong></div><div><strong>&nbsp;<span style="font-size: 1rem;">Always aim high!&nbsp;</span></strong></div><div>
</div><div><strong>PREPARING FOR EXAMS - 4-6 months before exams:
</strong></div><div>&gt; Always Listen + sit in the front half of the class (especially if teacher is good + respect him).
</div><div>&gt;Take Notes which are very important, If you don't, you forget and the lecture would be a waste of time for you.
</div><div>&gt; How to deal with boring, bad teachers? Simply Always stay ahead of them... yes, study from net and Notes but make sure you use time in their class wisely... 
</div><div>&gt;Review what you studied at least once before next class.
</div><div>&gt;Try to find companions who have good study habits as well.
</div><div>&gt;Organize your time so that you have time to play plus study... not kidding it's important to play... your play can be a constructive hobby too.
</div><div>&gt;When there are about 3-4 months left in exams MAKE A TIMETABLE!!! A timetable so that you can finish making notes and doing at least 8-10 years past papers. 
</div><div>Making a Study Plan (Time Table)
</div><div>&gt; Fill it with your goals for each day, e.g. on Monday, I'd revise “Incomplete Records” for Accounting, “Market Failure” for Economics and do past paper for Physics
</div><div>&gt; Make a blend such that you don't get bored due to monotony... (like study Physics, do half your goal if not full, then Accounting for 45 minutes and then Economics for 45 minutes. Just don’t let yourself get bored)
</div><div>&gt; After you have completed that day's goals you may use your computer, go hunting or whatever... just remember to set your goals realistic yet a tad bit challenging.
</div><div>&gt; Make your own summarized notes, hence bullet form would help.
</div><div>&gt; As you complete past papers modify your notes to add important points from mark schemes in your notes. This is VERY important.
</div><div>&gt; Make flashcards and write down the mark scheme points for the questions you could not attempt.
</div><div>&gt;For the important stuff you can’t remember, like formulas, write in bold and colorful writing and stick to a wall near your study table. Every time, you’d look at them, they’ll get revised and you will have no trouble in memorizing. <strong>GOOD LUCK!
</strong></div><div><strong><br></strong></div></div>
