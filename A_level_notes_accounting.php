</p><p><strong>Provisions:
</strong></p><p>IAS 37 establishes the definition of a provision as a "liability of uncertain timing or amount", and requires that all the following conditions be fulfilled before a provision can be recognized:
</p><p>	The entity currently has a liability as a result of a past event;
</p><p>	An outflow of resources is likely to be needed to settle the liability; and
</p><p>	The amount of the obligation can be estimated reliably
