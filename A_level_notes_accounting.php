<section class="mbr-section article content1 cid-qZdK4g9RPX" id="content1-6m">
    
     

    <div class="container">
        <div class="media-container-row">
            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><strong>Reserves
</strong><div>The net assets of the company are represented with capital and reserves. While Capital represents the claim that owners have because of the number of shares they own, reserves represent the claim that owners have because of the wealth created by the company over the years but not distributed to them. There are two main types of reserves.
</div><div>Revenue Reserves: The reserves which arise from profit (Trading activities of the company) . These are transferred from the Appropriation Account. Examples include General Reserve and Retained Profit (Profit and Loss) .Dividends can only be paid to the amount of revenue reserves on the balance sheet. I.e. the maximum dividend possible is the sum of both revenue reserves.
</div><div>Capital Reserves:These are reserves which the company is required to set up by law and cannot be distributed as dividends. They normally arise out of capital transactions. These include Share Premium and Revaluation Reserve and also Capital Redemption Reserve (See details of CRR)</div><div><br></div><div>
</div><div><strong>Revaluation Reserve
</strong></div><div>This is created when the value of an asset is increased in the books due to a permanent increase in market value. The amount of revaluation reserve is difference between net book value at the time of revaluation and the market value. This is a gain which cannot be transferred to the profit and loss account as it is still not realized (earned) by the company. This reserve can be used in the future if the same asset (on which the reserve was created) value goes down ( the loss can be written off against this reserve). This can also be used for Bonus Issue
.</div><div><br></div><div><strong>Share Premium
</strong></div><div>Share premium occurs when a company issues shares at a price above its nominal (par) value. This excess of share price over nominal value is what is known as share premium.
</div><div><br></div><div><strong>What are the uses of Share Premium?
</strong><span style="font-size: 1rem;"></span></div><div><strong>&gt;&gt;</strong> Issue Bonus Shares
</div><div><strong>&gt;&gt;</strong> Write off Formation ( Preliminary Expenses)
</div><div><strong>&gt;&gt;</strong> Write off Goodwill
</div><div><br></div><div><strong>What are the different Types of Preference Shares?
</strong></div><div>&gt;&gt; Non Cumulative Preference Shares: In case company doesn’t have enough profits these shareholders will get no dividend in the year and that amount of dividend will never be given
</div><div>&gt;&gt; &nbsp;Cumulative Preference Shares : : In case company doesn’t have enough profits these shareholders will get no dividend in the year and that amount of dividend will be carried forward to next year , when the company makes enough profit the entire amount will be payable as dividend.
</div><div>&gt;&gt; &nbsp;They are recorded as non current liabilities and the dividends paid to them are treated like interest ( finance cost)
</div><div><br></div><div><strong>ISSUE OF SHARES
</strong></div><div><strong>Public Issue:</strong> This is normal issue of shares to general public. A company can issue shares to public to raise more capital , this is done at the market price. Public issues have higher cost of issue ( this means the company has to incur high expenses when issuing the shares I.e. advertising and administration ). The main advantage of issuing shares is that no interest has to be paid on it and the company only have to provide a return when they actually make profits.
</div><div><br></div><div><strong>Rights Issue :</strong> A rights issue represents the offer of shares to the existing shareholders in proportion to their existing holding at a lower price compared to the market value.
</div><div><br></div><div><strong>Advantages of Rights Issue over Public issue
</strong></div><div>&gt;&gt; &nbsp;Rights issue are cheaper to administer and less risky way of raising capital
</div><div>&gt;&gt; Shareholders will get some incentive as they will get shares at a lower price.
</div><div><br></div><div><strong>Disadvantages</strong>
</div><div>&gt;&gt; &nbsp;Market price will fall
</div><div>&gt;&gt; The company could have raised more funds through a public issue
.</div><div><br></div><div><strong>Bonus Issue:</strong> Is the issue of shares to existing shareholders for free .When the company is short of cash and can’t give dividends so they give out shares for free to the ordinary shareholders. Other reasons for bonus issue include.
</div><div>&gt;&gt; To utilize the capital reserves
</div><div>&gt;&gt; To increase confidence in the company’s future prospects as it is normally taken as a signal of strength by the general public.
</div><div>&gt;&gt; &nbsp;When doing bonus issue company will always use capital reserves first and then the revenue reserves i.e. We can use either of revaluation reserve or share premium first but if we don’t have enough balance in both of these reserves then we will move to 
</div><div>&gt;&gt; General Reserve
</div><div>&gt;&gt; &nbsp;Profit and Loss.
</div><div><br></div><div><strong>WHAT IS CONVERTIBLE DEBENTURE/CONVERTIBLE LOAN STOCK?
</strong></div><div>Special type of debenture which can be converted into shares at a specified date. Upon conversion the debenture holder receives ordinary shares and he gives away is debenture certificate. The shares are sold to them in return of debentures, so that’s usually done at market price of share ( so share premium will be involved) . </div><div><strong>For example
:&nbsp;</strong><span style="font-size: 1rem;">A company has convertible loan stock worth $60000. They decided to convert it into shares by issuing 10 Ordinary shares of $1 each for every $15 of debenture. This means company will issue 40000 shares to settle the debenture , each share which is for $1 was sold for $1.5 .&nbsp;</span></div><div><strong>Debit:</strong> Debenture 60000
</div><div><strong>Credit:</strong> Ordinary Shares 40000 Share premium 20000
