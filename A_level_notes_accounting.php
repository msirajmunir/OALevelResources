</p><p><strong>Accounting Errors:</strong> If errors from previous periods are discovered later then they should be accounted for Retrospectively  (this means all the account balances from previous years should be adjusted)
</p><p><strong>IAS 10 – Events occurring after the Balance Sheet Date ( Post Balance Sheet Events)
</strong></p><p>These are events, either favorable or unfavorable, which occur between the balance sheet date and the date on which the financial statements are authorized for issue. Such items may occur as a result of information which becomes available after the end of the year and, therefore need to be disclosed in the accounts.
</p><p>The key is the point in time at which changes to the financial statements can be made. Once the financial statements have been approved for issue by the board of directors they can not be altered. For example, the accounts are prepared up to 31 December. They are approved for issue by the board of directors on 30 April in the following year. Between these two dates, changes resulting from events after the 31 December can be disclosed in the accounts.
</p><p><strong>The statement distinguishes between two types of events:
</strong></p><p><strong>Adjusting events
