<section class="mbr-section article content1 cid-r0jHUyusqW" id="content1-78">
    
     

    <div class="container">
        <div class="media-container-row">
            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><p>Product costing requires an accurate measurement of the resources consumed to manufacture a product. If a product is undercharged it may result into a loss to the business. Contrary to that if a customer is overcharged, there are ample chances that the business may not be able to compete and lose a customer.
</p><p>The conventional absorption costing method absorbs support overheads simply on production volume measured in terms of labor hours worked or number of units produced. In case the proportion of support overheads is quite low in total cost of a product and the manufacturing process is labor intensive, as a result, the direct costs would have been much higher than indirect costs and it may have an insignificant impact on realistic product pricing.
</p><p>However, the modern manufacturing process has become more machine intensive and as a result the proportion of production overheads have increased as compared to direct costs, therefore it is important that an accurate estimate is made for the production overheads per unit.
</p><p>The activity based costing (ABC) adopts more realistic approach to charge production overheads to determine the product cost. It charges overheads on the basis of benefits received from a particular overheads. It considers the relationship between the overheads costs and the activity which causes incurring of such costs, known as cost drivers.
</p><p><strong>Steps to calculate cost of production using ABC
</strong></p><p><strong>There are following five basic steps:
</strong></p><p>	Classify production overheads into activities according to how they are driven
</p><p>	Identify the cost driver for each activity, which causes these activities to incur the costs.
</p><p>	Calculate an overheads absorption rate (OAR) for each activity.
</p><p>	Absorb the activity costs into the product based on the benefits received by the production process.
</p><p>	Calculate the total cost of the product.
</p><p><strong>Uses of Activity Based Costing
</strong></p><p>	It is used to calculate a more accurate and realistic cost of a product.
</p><p>	It is used in an organization where production overheads form a significant portion of total cost of production.
</p><p>	It gives a better insight to consider what factors drive overheads costs.
</p><p>	It recognizes that all overhead costs are not related to production and sales volume.
</p><p>	It is used to control overhead costs by managing cost drivers.
</p><p>	It is used to apply on all the overhead costs not only the production overheads.
</p><p>	It is used just as easily as it is used in product costing.
</p><p><strong>Limitations of Activity Based Costing
</strong></p><p>	It has limited benefit if the overhead costs are a small proportion of the total costs.
</p><p>	The choice of both activities and cost drivers might be inappropriate.
</p><p>	It is more complex method of calculating the cost of a product.
</p><p></p></div>
