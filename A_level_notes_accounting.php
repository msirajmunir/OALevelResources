            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><p>A budget is based on the objectives of a business and enables the manager to set operational targets for the department and then to control operations by comparing the actual results with those in the budget. 
</p><p><strong>ADVANTAGES OF PREPARING BUDGETS :</strong>
</p><p>	Budgets formalize management plans.  ( better planning)Co-ordinate all functions of a business. ( better planning)
</p><p>	Gives a warning for future shortages of resources.
</p><p>	Increases management participation ( while preparing budgets) which gives them a sense of commitment …&gt; motivation
</p><p>	Budgeted results can be compared with actual results
</p><p>	Cash budgets are required by financial institutions when taking finance ( loans)
</p><p><strong>DISADVANTAGES OF PREPARING BUDGETS:
</strong></p><p>	Might cause de-motivation for workers if they feel budgeted figures are way too high to achieve
</p><p>	A budget will only emphasize on results and the real reasons ( non financial) will be ignored.
</p><p>	The budgeting process will also incur cost and time.
</p><p>	There is a need to revise the budget because circumstances will change in every period.
</p><p>What is a master budget?
</p><p>A set final of accounts (profit and loss and balance sheet) prepared using figures from sales, purchases and cash budget.
</p><p><strong>TYPES OF BUDGETING
</strong></p><p>Incremental Budgeting: Incremental Budgeting uses a budget prepared using a previous period’s budget or actual performance as a base, with incremental amounts added for the new budget period. The allocation of resources is based upon allocations from the previous period. This approach is not recommended as it fails to take into account changing circumstances
</p><p>Zero- Based Budgeting: is a technique of planning and decision-making which reverses the working process of traditional budgeting. In traditional incremental budgeting, departmental managers justify only increases over the previous year budget and what has been already spent is automatically sanctioned. By contrast, in zero-based budgeting, every department function is reviewed comprehensively and all expenditures must be approved, rather than only increases. No reference is made to the previous level of expenditure. Zero-based budgeting requires the budget request be justified in complete detail by each division manager starting from the zero-base. The zero-base is indifferent to whether the total budget is increasing or decreasing
</p><p><strong>What is a principal budget factor?
</strong></p><p>Limiting factors, sometimes called principal budget factors are circumstances which restricts the activities of a business. Examples include
</p><p>	Limited demand for a product
</p><p>	Shortage of material, which limits production
</p><p>	Shortage of labor, which also limits production
</p><p>	Shortage of amount of money to be spent.
</p><p>The importance of limiting factor is that they must be identified at the start of the budgeting process. This is because all other budgets will be dependant on the limit factor. For example, if the limiting factor is demand for the product ( which is usually the case), a sales budget must be prepared and all the other budgets will than be prepared to fit in with the sales budget. But if the limiting factor is shortage of material or labour then the production budget will be prepared first and the sales budget will then be based on that.
</p><p>What steps can be taken if the cash forecast highlights future cash shortages? Please realize every action will have a disadvantage as well. So the company decides the best possible action with least effect .
</p><p>	Delay capital expenditure for a later period
</p><p>	Delay payment of dividends
</p><p>	Search for short term borrowings
</p><p>	Issue shares
</p><p>	Control expenses
</p><p>	Encourage debtors to pay earlier
</p><p>	Sell Surplus fixed assets
</p><p>	Negotiate better credit terms with suppliers
</p><p><strong>What is Flexible Budget and Fixed Budget? 
</strong></p><p>A flexible budget is a budget which is designed to change in accordance with the LEVEL OF ACTIVITY actually produced. The budget is designed to change appropriately with such fluctuation in units. Main purpose of this is to take effect of VOLUME away from the budget so that we can compare it with actual performance.
</p><p>A fixed budget, the budget remains unchanged irrespective of the level of activity actually attained. The fixed budget is prepared based only on one level of output. Fixed budget approach helps to ensure that each department within the organization always knows exactly how much they have to spend at the beginning of the period and how much is remaining at any given point during the budgetary period as the target is pre-set this may increase motivation
