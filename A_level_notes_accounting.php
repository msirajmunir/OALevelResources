            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><div>The shareholders are the owners of the public limited company, but they are not permitted to manage their company unless they qualify as a director. The shareholders elect a Board of Directors and delegate the authority to them. As there is a divorce between ownership and control, it is a legal requirement for all companies to publish the financial statements for the use of shareholders. The companies publish the accounts in form of an ANNUAL REPORT.</div><div><br></div><div><strong>CONTENTS OF ANNUAL REPORT:
