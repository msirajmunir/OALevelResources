</strong></p><p>A present obligation that arises from past events but is not recognized because the timing or amount cannot be estimated reliability . They should not be recognized but disclosed to shareholders unless the chances of them are very rare(remote) then they can completely ignored.
</p><p><strong>Contingent Assets:
</strong></p><p>A possible asset arising from past events but will be confirmed in the future. They are disclosed if the chances of occurrence are high.
