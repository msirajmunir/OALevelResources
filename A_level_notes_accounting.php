            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><p>Auditing is normally associated with the accounts of limited companies. It is compulsory that larger limited companies have their financial statements audited by external auditors.
</p><p>An external auditor investigates whether or not the business has kept adequate records, that financial statements are consistent with the records from which they are prepared and that financial statements prepared by management give a true and fair view of the business’s state of affairs. The auditor verifies that transactions have actually taken place and that they have been recorded in the books of account accurately.
</p><p>The audit provides the users of financial statements with an assurance that the statements provided to them can be relied upon.
</p><p>The auditor’s opinion
</p><p>If, in the auditor’s opinion, adequate records have not been kept of the company’s financial statements or the auditable part of its directors’ report is not in agreement with the company’s records and returns, the auditor will issue a qualified opinion to the report.
</p><p>A qualified report will raise points that the auditor considers have not been dealt with correctly by the directors in their preparation of the financial statements.
</p><p>Where such points are not of a serious nature, the report might state ‘...with the exception of ... the financial statements do show a true and fair view ...’ If, however, the auditor is of the opinion that there has been a serious breach the statement will state that ‘the financial statements do not show a true and fair view’.
</p><p><strong>The Audit Report
</strong></p><p>The auditor’s report has three main sections:
</p><p>	Responsibilities of directors and auditors
</p><p>a Directors are responsible for preparing the financial statements.
</p><p>b Auditors are responsible for forming an opinion on the financial statements
</p><p>	Basis of opinion – the framework of auditing standards within which the audit was conducted, other assessments and the way in which the audit was planned and performed. If the auditor fails to obtain information and explanations necessary to support his audit then this must be reported. Any deviation from the necessary disclosure requirements must be identified.
</p><p>	Opinion – the auditor’s view of the company’s financial statements.
</p><p><strong>Audit requirements
</strong></p><p>The main statements that have to be audited are:
</p><p>	The income statement
</p><p>	The statement of financial position 
</p><p>	Statement of cash flows
</p><p>	Statement of total recognized gains and losses reconciliation of movements in shareholders’ funds
</p><p>	Accounting policies.
</p><p>	Notes to the accounts.
</p><p><strong>True and fair view
</strong></p><p>A true and fair view requires that the auditor must give an opinion as to whether the financial statements presented to the shareholders are truthful and unbiased.
</p><p><strong>The auditor must verify that:</strong></p><p>● Financial statements agree with company records. They must confirm that:
</p><p>● Results shown in income statements are truly and fairly stated
</p><p>● Fundamental accounting concepts have been applied
</p><p>● The accounting convention followed in the preparation of financial statements is stated
</p><p>● The preparation of financial statements is consistent with previous periods.
</p><p><strong>The auditor must verify that:
</strong></p><p>● Assets exist and are owned by the company and are stated at amounts that are in accordance with accepted accounting policies
</p><p>● All liabilities are included and stated at amounts that are in accordance with accepted accounting policies.
</p><p></p></div>
