</div><div><strong><br></strong></div><div><strong>What is a partnership change? 
</strong></div><div>
</div><div>A partnership change occurs when there is a change in structure of the partnership . The partnership is not dissolved it is only changed. i.e Admission of a new partner, Retirement of an existing partner , or simply a change in existing profit sharing ratio. 
</div><div>
</div><div><strong><br></strong></div><div><strong>Why do we have to treat for goodwill in a partnership change? 
</strong></div><div>
</div><div>Any goodwill generated till date belongs to the old partners. So the goodwill adjustment is done in such a way that old partners will benefit and the new partners will loose out. </div><div>This is because goodwill is kept in line with the profit sharing ratio.</div><div><br></div><div>The new partner ends up paying for goodwill and the old partner if he is leaving gets paid for his goodwill. This way all partners are treated fairly.
</div><div><strong><br></strong></div><div><strong>How do we have to treat for goodwill in a partnership change?
</strong></div><div>There are two methods to treat goodwill
</div><div><strong><br></strong></div><div><strong>Method 1: Goodwill is kept in the books
</strong></div><div>In this method we create goodwill in the old profit sharing ratio in capital accounts and leave it ( so that it can be shown in the balance sheet as a non current asset). This method is rarely used and is not prefered because it’s not in line with the Prudence and Money Measurement Concept.</div><div><br></div><div>
</div><div><strong>Method 2: Goodwill is Created but then written off right away
</strong></div><div>In this method we create goodwill in the old profit sharing ratio in capital accounts but then write it off in the new profit sharing ratio. This method is frequently used and follows Prudence and Money Measurement Concept.
</div><div>Note: If Question doesn’t specify clearly always use method 2</div><div><br></div><div>
</div><div>
</div><div><strong>What if Goodwill is already recorded on the balance and has to be adjusted?
</strong></div><div>The amount of goodwill on the balance sheet is already in capital accounts of the partner so we only need to create the difference ( increase ) in the goodwill in old profit sharing ratio ( and then write off the entire amount if required to write it off. Alternate method would be to treat the change in goodwill in revaluation account and then write i off the full amount from the capital account.</div><div><br></div><div>
</div><div><strong>What is the difference between revaluation account and realization (dissolution) account?
</strong></div><div>Revaluation account is made at the time of change in a partnership ( see above) . This is done to change the values of asset to the current market value so that any gain or loss that has arise before the change can be adjusted in capital of partners. When making revaluation account if only take the changes in assets ( the difference in values) and close it off in the old profit sharing ratio
</div><div>Realization account is made when the partnership business is dissolved or sold . The aim of this account is to calculate the overall gain or loss upon closure of the partnership business. In this we first close the assets at net book value and compare it with the amount realized upon sale . The difference (overall gain or loss) is closed off in the profit sharing ratio as well.
</div><div><strong><br></strong></div><div><strong>What are the possible reasons for dissolution in a partnership?</strong></div><div><strong>
</strong></div><div><strong>&gt;&gt;</strong> &nbsp;Business is making losses and future prospects are not good.
</div><div><strong>&gt;&gt;</strong> &nbsp;Death/Retirement of a partner ( Specially in case of two partners in a partnership)Legal</div><div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span style="font-size: 1rem;">authorities have forced the business to close down</span><br></div><div><strong>&gt;&gt;</strong> &nbsp;Dispute between the partners
</div><div><strong>&gt;&gt;</strong>	Business is very illiquid and about to get bankrupt
</div><div><strong>&gt;&gt; </strong>&nbsp;Partners want to form a limited company.</div><div><br></div><div>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong><u>PURCHASE AND SALE OF BUSINESS</u></strong></div><div><strong><u><br></u></strong></div><div>Purchased Goodwill is calculated by the company which is buying the business. The formula used is
