<section class="mbr-section article content1 cid-qX2c2fBPmS" id="content1-39">
    
     

    <div class="container">
        <div class="media-container-row">
            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7">We all know what our weaknesses are and our strengths are. First list all the skills you think required for writing.
<div>
</div><div>Here let me get you started-
</div><div>
</div><div><strong>•	A captivating Introduction
</strong></div><div><strong>•	A body , covering the right information
</strong></div><div><strong>•	A Conclusion
</strong></div><div><strong>•	Proper paragraphing
</strong></div><div><strong>•	Grammar
</strong></div><div><strong>•	Punctuation
</strong></div><div><strong>•	Spelling
</strong></div><div><strong>•	Plot or outline
</strong></div><div><strong>•	Tone
</strong></div><div><strong>•	Format
</strong></div><div><strong>•	Coverage of points given
</strong></div><div><strong>•	Appropriateness
</strong></div><div><strong>•	Word limit
</strong></div><div><strong>•	Sentence variation</strong>
</div><div>
</div><div>Go through these books, you’ll find plenty of skills and helps on how to master them. I’m sure most of you are pretty overwhelmed ‘OMG so many skills and we have to cover them all!’ Relax, take a breathe.
</div><div>Most of them you will have mastered by now through out school years. But I’m sure you can identify where you lack. It’s oaky even if its many of them, some are quite easy to get the hang of.
</div><div>How to be the Master?
</div><div>&nbsp;
</div><div>Simple guys, tackle one or two skills at a time. A great way is solve an exercise related to it Just familiarize yourself with it , the next day or later pick a topic form your past papers or the topic your teacher gave you . Keep the skill or the two skills in mind and write on the topic. While you write stop and check are you using that skill’?
</div><div>
</div><div>My own example is that I had terrible spelling and wrote long detailed sentences. First I solved the exercises from the above books for spelling (I worked on it on the side as well) and sentence variation. Another tip guys, observe your surrounding and churn your brain for story ideas. Always keep some story idea, some made-up plot at hand, in a section of you register jot down the ideas whenever you get them. Now that I needed to practice a skill, I simply opened my idea section and picked one randomly and I started writing. As I wrote, I consciously checked whether I was making any spelling mistake or too many long sentences.
</div><div>
</div><div>5.	Spend at least half an hour or one everyday on English, aside from your school work. That’s it, nothing more. You can easily skip a day or two every now and then. It’s like a session, in each session do one thing. In one session solve an exercise from one of the books, in the next use that skill and practice it in a piece of writing, in third do a comprehension passage.
</div><div>6.	Good Vocabulary is very important. I repeat very important. But some things to remember are that don’t use too difficult words, like from an old English book, which no one uses. Get your vocabulary words from a list of SAT words (a very useful way).Every few sessions , have a session in which you spend time writing sentences for a particular few vocabulary words , it’s a great way to get acquainted with the word and you’re sure to use it while writing next time. Idioms are also very useful, do the same for them.
</div><div>7.	By now you should’ve started to get the hang of English, let’s start some past papers. Now, the trick for past papers is quite simple. Though I would recommend do as many as you can, there is also one more important factor that leads to a good grade.
</div><div>
</div><div>Listen up, for the Reading paper:-
</div><div>
</div><div>I.	Ask your teacher to check your paper in front of you, ask her /him why they cut the mark, what the correct answer is and why? Then ask how do we get that answer?
</div><div>II.	Solve some papers on you own and have the marking scheme at hand along with the examiner report if you can get it. Do one paper at a time and then mark yourself honestly according to the mark scheme, now here most of us just leave off. No, look at the correct answer; observe why you made that mistake? Did you not understand the question or misread? Etc. Read the examiner report and you’ll find out what exactly the Examiner asks for, you’ll see at what place you stand. Give it some time and do the paper again, at least the questions you did wrong. Then check again, did you make the same mistake? Do it until you get it right. No , I don’t mean you guys to rote memorize the answer .By doing it again you’ll be correcting yourself and your brain will remember the correct way to solve it not the wrong one. Or else you will most probably end up making the same mistake.
</div><div>III.	The same goes for the note-taking part. Going through the marking scheme when you do a paper is the best way to get the hang of it.
</div><div>&nbsp;
</div><div>
</div><div>For your writing exam:-
</div><div>I.	Yes it’s all about practice, scribble-scribble that’s all you have to do. But again even here though there’s not much to refer to but have your work checked. Find out your weaknesses , see a lot of us simply keep on writing but never stop to check if we’re in the right direction or not.
</div><div>II.	The secret here is, find your best writing style for Section B. For example if you’re good at stories, narratives etc, stick to that. Become a master; identify all the techniques needed in it; catchy intro; summarizing conclusion; word limit etc. Work on them one by one. Don’t leave it to the last minute, that you’ll just choose whatever topic you like in the exam and do that. No that would be a great mistake, you might like the topic for an argumentative but you might not be so good at it and thus lose your grade. A good argumentative essay requires you to have a strong convincing voice (tone); you need to be well aware of the current affairs and some statistical knowledge as well. So choose wisely.
</div><div>
</div><div>I think I’ve told you guys quite enough, just a few more general tips just so nothing is overlooked or forgotten.
</div><div>•	Print out a copy of the syllabus and see for the writing exam you’re checked according to bands. Identify with the help of your teacher or whoever checks your work at which band is your work and why? See how you can take it to the next level.
</div><div>•	In your register note down a neat copy of all the formats from a trusted place. Look up tips for the different types of writing; formal letter; informal letter; account writing; report; etc. And refer to it when you start doing them.
</div><div><br></div><div>Read other people’s work, the good and bad but not too much okay. Just see how everyone else is doing, are you making the same mistake as the poor writer is? Are you doing what a good writer is? But don’t copy or judge, just get an idea okay.
</div><div>
</div><div>This guide is basically for those who don’t know what to do and are at lost. Now hopefully you know    what to do and with a bit of effort you’re good to go for the exam.
</div><div><br></div></div>
        </div>
    </div>
