            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7">A* in English, seems impossible for most of us? Right? But doesn’t the word itself say I’m Possible? It is, dear reader it is :) 
<div>Having gotten an A* myself, I discovered a few secrets which sadly most of us overlook. Right, so let’s get started.
 &nbsp;</div><div><br></div><div>First let me clarify something, you need to be serious. No matter what the subject is you have to be serious, no doesn’t mean you can’t have fun. Of course the best way to learn is by enjoying the subject. But what I mean is, Guys, the struggle is your own. It’s called Personal effort, unless you’re not ready to sacrifice your sleep; a few hours of face book; doing more than just what the teacher says; then am sorry, even reading this guide is pointless. So be ready to make an effort, otherwise prepare yourself to accept a lower grade.
</div><div>
</div><div>Now with the right kind of audience left with us, the losers probably left of by now, let’s ROLL. English language is by all means a tough subject but very easily mastered if you tackle it right. Let’s start with the basics
</div><div>Rules
</div><div>
</div><div>1.	Buy a separate register for English only. It should be personal, no school work done on it. Only practice.
</div><div>2.	Buy a small personal thesaurus and Dictionary
</div><div>3.	You need to have a good reading habit. Most probably English isn’t your first language, so I bet my hat you teacher probably complains about your incorrect tenses. No use buying those silly books, chock full of brainless sentences. You’ll never get them.
</div><div>
</div><div>
</div><div>
</div><div>Some tips while you read
</div><div>
</div><div>•	Pick a good author. E.g. Thomas Hardy , Charles Dickens , Poelho Coelho , Khalid  Hossiani , Dan Brown etc
</div><div>•	Pick a book recommended by a teacher or a good reading friend or one you have heard has &nbsp; &nbsp; &nbsp;literature. Example – The Kite Runner , Tess of D’Urbervilles , Jane Eyre , even a light reading of &nbsp;Harry potter , Hunger Games , Twilight series , Divergent Series , etc is okay they are all very &nbsp; &nbsp;well written and easily available.
</div><div>•	Don’t pick an oldie moldy yellowing classic with old English, get a newer version.
</div><div>•	Have pencil ,you English register and dictionary nearby
</div><div>•	Read consciously, don’t stop at every single word you don’t understand, skip the ones where you get the idea of what it means from the context. But underline the ones you don’t understand and look them up , jot it all down
</div><div>&nbsp;
</div><div>•	Also as you read, look for interesting phrases, e.g. Tears clung to her lashes like pearls, I am sure you’ll find a lot better than this. Jot them down too.
</div><div>
</div><div>Note – Jot all of this separately. Try to get a ringed register with dividers. In one section have all the vocabulary words, in the other the phrase / sentences /even whole paragraphs whatever you like.
</div><div>•	Last but not the least enjoy the story 
</div><div>
</div><div>
</div><div>4.	Now get into the habit of writing. As you keep up your reading habit read your writing expression will improve tremendously. Writing is important .I repeat very important, because it is the only way you can prove to the examiner you deserve an A*.
</div><div>Some tips for writing
</div><div>
</div><div>•	Start a dairy/ journal or anything. Just get the hang of writing. Believe it’s a great stress releaser, writing stuff down. Not to mention how it de-clutters your mind and your able top see things more clearly.
</div><div>•	Google up some interesting writing forums, join them. Become an active member.
</div><div>•	Start a blog.
</div><div>•	Make a list of topics, use old past paper topics or story ideas easily available on the web. Get started; write on one topic everyday or at least two or three topics in a week.
</div><div>
</div><div>You don’t need to follow every one of these tips but a few are enough, lets hope you’re already following some.
</div><div>Now let’s sink our teeth into the subject itself. How to the Exam?
</div><div>
</div><div>Reading a few books or writing a lot is not enough to get you the grade. That’s just to give you a base, a foundation. The actual exam, believe it or not is a lot more technical. I’m not going to get into the tedious business of telling you guys the formats in this guide , that’s just a small part and you can get those on your own, plenty available .
</div><div>
</div><div>Now I want to share a secret with you guys. In plain words English is Language. We all know that but what we don’t know is that it has many small skills. It starts from reading and writing. Which actually branch to countless small skills, which is precisely what the CIE examiner is looking for? They check whether or not you have the skills or not. Knowing this piece of information can lead a great way, with this in hand you can start your preparation for the exam.
</div><div>
</div><div>I hope you all have these two books (pasted below); going through them is a great way to identify the skills needed and work on.
</div><div><strong><br></strong></div></div>
