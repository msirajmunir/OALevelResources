            <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7"><strong><u>1. Salutation 
</u></strong><div>The most common salutation in an informal letter is “Dear….” 
</div><div>Note that it is followed by a comma. 
</div><div>Dear Mimi, 
</div><div>Always try to use the nick name or the first name of the person you’re writing to, in the informal letters. 
</div><div>However, some go extremely informal and use “Hey!” or “Hi!” such type of use is not allowed in the examination. 
</div><div><strong><u>2. Body </u></strong>
</div><div>Here are a few things you should take note of: 
</div><div>1. Use informal language 
</div><div>The first paragraph generally expresses a greeting, followed by wishes of good health. Remember you are writing to someone you know very well, so try to be as friendly as possible but don’t become so much friendly and you start using the extreme use of colloquial words and abusing words. A good start: 
</div><div><strong>“How are you my dear sister?”</strong> 
</div><div>Try not to go overboard. Some people become bold and daring in letters and write things that they would otherwise not say to the person face to face. Obviously if you are writing to an adult that you respect, like your dad, try not to write things like: 
</div><div><strong>“What’s up dude!” or “What’s going down?” 
